class Dictionary:
    """Dictionary implementation."""

    def __init__(self):
        """Initializes the dictionary."""

    def set(self, k: int, v: int) -> None:
        """Set the value of the item with key k to v."""

    def get(self, k: int) -> int:
        """Return the value of the item with key k if exist, else return -1."""

    def remove(self, k: int) -> None:
        """Removes the item with key k from the dictionary."""
